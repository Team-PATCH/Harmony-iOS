import SwiftUI

extension Font {
    static func pretendardRegular(size: CGFloat) -> Font {
        return Font.custom("Pretendard-Regular", size: size)
    }
    
    static func pretendardBold(size: CGFloat) -> Font {
        return Font.custom("Pretendard-Bold", size: size)
    }
    
    static func pretendardExtraLight(size: CGFloat) -> Font {
        return Font.custom("Pretendard-ExtraLight", size: size)
    }
    
    static func pretendardLight(size: CGFloat) -> Font {
        return Font.custom("Pretendard-Light", size: size)
    }
    
    static func pretendardThin(size: CGFloat) -> Font {
        return Font.custom("Pretendard-Thin", size: size)
    }
    
    static func pretendardExtraBold(size: CGFloat) -> Font {
        return Font.custom("Pretendard-ExtraBold", size: size)
    }
    
    static func pretendardBlack(size: CGFloat) -> Font {
        return Font.custom("Pretendard-Black", size: size)
    }
    
    static func pretendardSemiBold(size: CGFloat) -> Font {
        return Font.custom("Pretendard-SemiBold", size: size)
    }
    
    static func pretendardMedium(size: CGFloat) -> Font {
        return Font.custom("Pretendard-Medium", size: size)
    }
}
